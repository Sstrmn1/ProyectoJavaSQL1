    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (mensajeConfirmacion("Desea editar este registro?") == 0) {
            String respuesta = "";

            int idLote = Integer.parseInt(txtId.getText());
            Articulo articuloSeleccionado = (Articulo) cboArticulo.getSelectedItem();
            Laboratorio laboratorioSeleccionado = (Laboratorio) cboLaboratorio.getSelectedItem();
            int articuloId = articuloSeleccionado.getIdArticulo();
            String articuloCodigo = articuloSeleccionado.getCodigo();
            String articuloDescripcion = articuloSeleccionado.getDescripcion();
            int laboratorioId = laboratorioSeleccionado.getIdLaboratorio();
            String laboratorioNombre = laboratorioSeleccionado.getNombre();

            String loteCodigo = txtCodigoLote.getText();
            int stock = Integer.parseInt(txtStock.getText());
            float precioUnitario = Float.parseFloat(txtPrecio.getText());

            boolean estado;
            if (rbtnActivo.isSelected()) {
                estado = true;
            } else {
                estado = false;
            }

            Calendar cal;
            Date loteFechaFab;
            Date loteFechaExp;
            int d, m, a;
            try {
                cal = dcFabricacion.getCalendar();
                d = cal.get(Calendar.DAY_OF_MONTH);
                m = cal.get(Calendar.MONTH);
                a = cal.get(Calendar.YEAR) - 1900;
                loteFechaFab = new Date(a, m, d);
            } catch (Exception e) {
                mensajeError(e.getMessage());
                loteFechaFab = new Date(2020 - 1900, 00, 01);
            }
            try {
                cal = dcExpiracion.getCalendar();
                d = cal.get(Calendar.DAY_OF_MONTH);
                m = cal.get(Calendar.MONTH);
                a = cal.get(Calendar.YEAR) - 1900;
                loteFechaExp = new Date(a, m, d);
            } catch (Exception e) {
                mensajeError(e.getMessage());
                loteFechaExp = new Date(2020 - 1900, 00, 01);
            }

            respuesta = this.CONTROL.actualizar(
                    idLote,
                    articuloId,
                    laboratorioId,
                    articuloCodigo,
                    articuloDescripcion,
                    loteCodigo,
                    loteFechaFab,
                    loteFechaExp,
                    stock,
                    precioUnitario,
                    laboratorioNombre,
                    estado);
            if (respuesta.equals("OK")) {
                mensajeInformacion("Registro actualizado");
            } else {
                mensajeError("Error insertando el registro");
            }
            limpiar();
            tabbGeneral.setSelectedIndex(0);
            this.listado("");
        } else {
            limpiar();
            desactivar();
            tabbGeneral.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

